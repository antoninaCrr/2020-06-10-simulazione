	public List<Actor> getAllVertices(String genre, Map<Integer,Actor> mappa){
		String sql = " SELECT DISTINCT a.id, a.first_name, a.last_name, a.gender "
				+ "FROM actors a, roles r, movies_genres mg "
				+ "WHERE a.id = r.actor_id AND r.movie_id = mg.movie_id AND mg.genre = ?";
		List<Actor> result = new ArrayList<Actor>();
		Connection conn = DBConnect.getConnection();

		try {
			PreparedStatement st = conn.prepareStatement(sql);
			st.setString(1, genre);
			ResultSet res = st.executeQuery();
			while (res.next()) {

				Actor actor = new Actor(res.getInt("id"), res.getString("first_name"), res.getString("last_name"),
						res.getString("gender"));
				mappa.put(actor.getId(), actor);
				result.add(actor);
			}
			conn.close();
			return result;
			
		} catch (SQLException e) {
			e.printStackTrace();
			return null;
		}

		
	}
	
	public List<Arco> getAllEdges(String genre, Map<Integer,Actor> mappa){
		String sql = " SELECT r1.actor_id AS a1, r2.actor_id AS a2, COUNT(*) AS peso "
				+ "FROM movies_genres mg, roles r1, roles r2 "
				+ "WHERE mg.genre = ? AND mg.movie_id = r1.movie_id AND mg.movie_id = r2.movie_id AND r1.actor_id > r2.actor_id "
				+ "GROUP BY r1.actor_id, r2.actor_id "
				+ "HAVING peso > 0";
		List<Arco> result = new ArrayList<Arco>();
		Connection conn = DBConnect.getConnection();

		try {
			PreparedStatement st = conn.prepareStatement(sql);
			st.setString(1, genre);
			ResultSet res = st.executeQuery();
			while (res.next()) {

				Actor a1 = mappa.get(res.getInt("a1"));
				Actor a2 = mappa.get(res.getInt("a2"));
				if(a1 != null && a2 != null) {
					Arco arco = new Arco (a1,a2,res.getInt("peso"));
					result.add(arco);
				}
			}
			conn.close();
			return result;
			
		} catch (SQLException e) {
			e.printStackTrace();
			return null;
		}
	}
	
	
	
	
	
	
	
